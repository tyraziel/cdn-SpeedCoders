local function collideWithHero( self, event )

  if ( event.phase == "began" and not self.isInvincible) then
    local destroyOther = false
  
    if(event.other.what == "Enemy" or 
       event.other.what == "EnemyBullet" or
       event.other.what == "Boss") then

      lifeTotal = lifeTotal - 1
      self.isInvincible = true
      self.invincibleTimeLived = 0
	
	  if(event.other.what ~= "Boss") then
        event.other:removeSelf()
      end

      if(lives < 1)then
        gameState.gameOver = true
      end

    elseif(event.other.what == "Item") then
      --powerup goodness?
    elseif(event.other.what == "blueKey") then
      self.blueKeys = self.blueKeys + 1
      destroyOther = true
    elseif(event.other.what == "redKey") then
      self.redKeys = self.redKeys + 1
      destroyOther = true
    elseif(event.other.what == "greenKey") then
      self.greenKeys = self.greenKeys + 1
      destroyOther = true
    elseif(event.other.what == "masterKey") then
      self.masterKeys = self.masterKeys - 1
      destroyOther = true
    elseif(event.other.what == "blueDoor" and self.blueKeys > 0) then
      self.blueKeys = self.blueKeys - 1
      destroyOther = true
    elseif(event.other.what == "redDoor" and self.redKeys > 0) then
      self.redKeys = self.redKeys - 1
      destroyOther = true
    elseif(event.other.what == "greenDoor" and self.greenKeys > 0) then
      self.greenKeys = self.greenKeys - 1
      destroyOther = true
    elseif(event.other.what == "win") then
      gameState.gameOver = true
      gameState.win = true
    end
  
    --TODO Remove me
    print( "HERO: "..self.what .. ": collision began with " .. event.other.what )
    
    if(destroyOther) then
      event.other:removeSelf()
    end
  end
  
end

