local function gameLoop(event)
  local timeSinceLastCall = event.time - gameTimer.lastTime
  local secondsElapsed = timeSinceLastCall / 1000
  local millisElapsed = timeSinceLastCall
  
  gameTimer.lastTime = event.time
--[[
  debugXText.text = math.floor(hero.x)
  debugYText.text = math.floor(hero.y)

  debugMGXText.text = math.floor(mainGroup.x)
  debugMGYText.text = math.floor(mainGroup.y)
]]--
  heroRedKeyText.text = "x "..hero.redKeys
  heroBlueKeyText.text = "x "..hero.blueKeys
  heroGreenKeyText.text = "x "..hero.greenKeys
  
  if(not gameState.endGame) then
    if(not gameState.gameOver) then
      if(not gameState.paused) then
  
        --only move guy if dpad_touched
        if (dpad_touched) then

          local dx = (centerDot.x - center_dot_x)
          local dy = (centerDot.y - center_dot_y)

          local val = -dy / dx
          local deg = 90-math.deg(math.atan(val))
          hero.deg = deg
    
          if((deg >= 45 and deg <= 135) or (deg >= 225 and deg <= 315)) then
  		    --hero:setLinearVelocity(dx*hero.rateOfMovement,0)
            hero.x = hero.x + dx * secondsElapsed * hero.rateOfMovement
            if(hero.x > logicalCoords.x + 400 and dx > 0) then
              mainGroup.x = mainGroup.x - dx * secondsElapsed * hero.rateOfMovement
              logicalCoords.x = logicalCoords.x + dx * secondsElapsed * hero.rateOfMovement
            end
            if(hero.x < logicalCoords.x + 80 and dx < 0) then
              mainGroup.x = mainGroup.x - dx * secondsElapsed * hero.rateOfMovement
              logicalCoords.x = logicalCoords.x + dx * secondsElapsed * hero.rateOfMovement
            end
          else
    	    --hero:setLinearVelocity(0,dy*hero.rateOfMovement)
            hero.y = hero.y + dy * secondsElapsed * hero.rateOfMovement
          
            if(hero.y > logicalCoords.y + 260 and dy > 0) then
              mainGroup.y = mainGroup.y - dy * secondsElapsed * hero.rateOfMovement
              logicalCoords.y = logicalCoords.y + dy * secondsElapsed * hero.rateOfMovement
            end
            if(hero.y < logicalCoords.y + 20 and dy < 0) then
              mainGroup.y = mainGroup.y - dy * secondsElapsed * hero.rateOfMovement
              logicalCoords.y = logicalCoords.y + dy * secondsElapsed * hero.rateOfMovement
            end
          
          end
      
        else
      	  --hero:setLinearVelocity(0,0)
      
        end -- DPAD Touched
      
        timeLeft = timeLeft - millisElapsed
      
        local minutesLeft = math.floor(timeLeft / 1000 / 60)
        local secondsLeft = math.floor((timeLeft - (minutesLeft * 1000 * 60)) / 1000 )
      
        if(secondsLeft < 10) then
          secondsLeft = "0"..secondsLeft
        end
      
        timeLeftText.text = minutesLeft..":"..secondsLeft
      
        --math.floor(timeLeft)
      
        if(timeLeft < 0)then
          gameState.gameOver = true
          timeLeft = 0
          timeLeftText.text = "0:00"
        end
      

      end
    else -- GAMEOVER
      if(gameState.win) then
        local winText = display.newText("YOU WIN!", 100, 115, native.systemFont, 64)
        winText:setTextColor(0,255,64)
      else
        local loseText = display.newText("GAME OVER!", 50, 115, native.systemFont, 64)
        loseText:setTextColor(255,255,0)
      end
      gameState.endGame = true
    end
  end
end

